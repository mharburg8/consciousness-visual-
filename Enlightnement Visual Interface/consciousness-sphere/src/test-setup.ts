import '@testing-library/jest-dom'

// jsdom does not implement window.matchMedia — stub it so hooks that
// read prefers-reduced-motion don't throw during unit tests.
Object.defineProperty(window, 'matchMedia', {
  writable: true,
  value: (query: string) => ({
    matches: false,
    media: query,
    onchange: null,
    addListener: () => {},
    removeListener: () => {},
    addEventListener: () => {},
    removeEventListener: () => {},
    dispatchEvent: () => false,
  }),
})
